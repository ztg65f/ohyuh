// Parse function to convert raw data to structured format
export function parseRoastData(): RoastData {
  const lines = roastTemplatesRaw.trim().split("\n")
  const data: Partial<RoastData> = {}
  let currentSection = ""

  for (const line of lines) {
    const trimmed = line.trim()
    if (!trimmed) continue

    // Check if this line starts a new section (no leading spaces)
    if (!line.startsWith("  ") && !line.startsWith("\t")) {
      currentSection = trimmed.toLowerCase()
      if (!data[currentSection as keyof RoastData]) {
        data[currentSection as keyof RoastData] = []
      }
    } else {
      // This is content for the current section
      if (currentSection && trimmed) {
        data[currentSection as keyof RoastData]?.push(trimmed)
      }
    }
  }

  return data as RoastData
}

